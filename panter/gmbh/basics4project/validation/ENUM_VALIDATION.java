	public String getButtonKey() {
		String buttonkenner = null;

		//201711124: validierung von menuecalls
		if (this.modul!=null) {
			buttonkenner = this.genDbVal(this.mainTheme,true)+this.modul.get_callKey();
		} else {
		
			if (this.code4Function!=null) {		  //freie variante
				buttonkenner = this.mainTheme.db_val()+"#"+this.code4Function;
			} else {
				buttonkenner = this.genDbVal(this.mainTheme,true)+this.genDbVal(this.subTheme,true)+this.genDbVal(this.function,false);
			}
		}
		return buttonkenner;
	}
	
	
	private String genDbVal(IF_enum_4_db en, boolean addHashTag) {
		String cRet = "";
		if (en != null) {
			cRet = en.db_val()+(addHashTag?"#":"");
		}
		return cRet;
	}
	
	
	private String getUserText(IF_enum_4_db en, String separator) {
		String ret = "";
		if (en != null) {
			ret = en.user_text();
			if (S.isFull(separator)) {
				ret = ret+separator;
			}
		}
		return ret;
	}
	
	private String getUserText(E2_MODULNAME_ENUM.MODUL en, String separator) {
		String ret = "";
		if (en != null) {
			ret = en.get_userInfo().COrig();
			if (S.isFull(separator)) {
				ret = ret+separator;
			}
		}
		return ret;
	}

	
