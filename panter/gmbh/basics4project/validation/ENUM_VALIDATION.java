	public static void writeMissungButtons2DB(MyE2_MessageVector  p_mv) throws myException {
		MyE2_MessageVector  mv = null; 
		if (p_mv==null) {
			mv = new MyE2_MessageVector();
		} else {
			mv = p_mv;
		}
		
		RecList20 rlButtons = new RecList20(_TAB.button)._fill(new SEL("*").FROM(_TAB.button)
																			.WHERE(new vgl(BUTTON.modulkenner, COMP.EQ, E2_MODULNAME_ENUM.MODUL.MODUL_KENNER_PROGRAMM_WIDE.get_callKey())).s());
		
		Vector<ENUM_VALIDATION>  vMissing = new Vector<>();
		ENUM_VALIDATION[]  vals = ENUM_VALIDATION.values();
		
		
		for (ENUM_VALIDATION v: vals) {
			boolean found = false;
			for (Rec20 r: rlButtons) {
				if (r.get_ufs_dbVal(BUTTON.buttonkenner).equals(v.getButtonKey())) {
					found = true;
					break;
				}
			}
			if (!found) {
				vMissing.add(v);
			}
		}
		
		//jetzt die fehlenden buttons schreiben
		for (ENUM_VALIDATION v: vMissing) {
			Rec20 recNew = new Rec20(_TAB.button);
			
			recNew._nv(BUTTON.modulkenner, E2_MODULNAME_ENUM.MODUL.MODUL_KENNER_PROGRAMM_WIDE.get_callKey(), mv);
			recNew._nv(BUTTON.buttonkenner, v.getButtonKey(), mv);
			
			recNew._SAVE(true, mv);
		}

		
		if (mv.get_bIsOK()) {
			mv.add_MESSAGE(new MyE2_Info_Message(new MyE2_String("Anzahl Buttonscode dazugefügt: ").ut(""+vMissing.size())));
		}
		
		//wenn kein messagevektor uebergeben wurde, dann wird werden die messages veroeffentlicht
		if (p_mv == null) {
			bibMSG.add_MESSAGE(mv);
		}
		
	}
	
	
	
	public static String getBeschreibung(String buttonCode) {
		String cRet = buttonCode;
		
		ENUM_VALIDATION[] allValid = ENUM_VALIDATION.values();
		
		for (ENUM_VALIDATION v: allValid) {
			if (v.getButtonKey().equals(buttonCode)) {
				cRet = v.getReadableText();
				if (S.isEmpty(cRet)) {
					cRet=buttonCode;
				}
			}
		}
		
		return cRet;
	}
	

	
	
