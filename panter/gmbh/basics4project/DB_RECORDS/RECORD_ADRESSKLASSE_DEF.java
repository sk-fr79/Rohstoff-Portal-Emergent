		private RECLIST_ADRESSKLASSE DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def = null ;






	/**
	 * References the Field ID_ADRESSKLASSE_DEF 
	 * Falls keine get_ID_ADRESSKLASSE_DEF_cUF() leer ist, wird null zurueckgegeben 
	 */
	public RECLIST_ADRESSKLASSE get_DOWN_RECORD_LIST_ADRESSKLASSE_id_adressklasse_def() throws myException
	{
		if (S.isEmpty(this.get_ID_ADRESSKLASSE_DEF_cUF()))
			return null;
	
		if (this.DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def==null)
		{
			this.DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def = new RECLIST_ADRESSKLASSE (
			       "SELECT * FROM "+bibE2.cTO()+".JT_ADRESSKLASSE WHERE ID_ADRESSKLASSE_DEF="+this.get_ID_ADRESSKLASSE_DEF_cUF()+" ORDER BY ID_ADRESSKLASSE",
			       this.get_oConn());
		}
		return this.DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def;
	}





	/**
	 * cZusatzWhere ODER NULL gibt eine weitere auswahl-Bedingung nach unten (z.B.NVL(DELETED,'N')='N')
	 * cORDERKEY ODER NULL bestimmt die sortierung des subselects
	 * buildNew  zwingt zum aufbau einer neuen subquery-list
	 * References the Field ID_ADRESSKLASSE_DEF 
	 * Falls keine get_ID_ADRESSKLASSE_DEF_cUF() leer ist, wird null zurueckgegeben 
	 */
	public RECLIST_ADRESSKLASSE get_DOWN_RECORD_LIST_ADRESSKLASSE_id_adressklasse_def(String cZusatzWhere, String cORDERKEY, boolean buildNew) throws myException
	{
		if (S.isEmpty(this.get_ID_ADRESSKLASSE_DEF_cUF()))
			return null;
	
		if (this.DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def==null || buildNew)
		{
		    String cQuery = "SELECT * FROM "+bibE2.cTO()+".JT_ADRESSKLASSE WHERE ID_ADRESSKLASSE_DEF="+this.get_ID_ADRESSKLASSE_DEF_cUF();
		    if (S.isFull(cZusatzWhere))
		    {
		    	cQuery += (" AND "+cZusatzWhere);
		    }
		    
		    if (S.isFull(cORDERKEY))
		    {
		    	cQuery += (" ORDER BY  "+cORDERKEY);
		    }
		    
			this.DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def = new RECLIST_ADRESSKLASSE (cQuery,this.get_oConn());
		}
		return this.DOWN_RECLIST_ADRESSKLASSE_id_adressklasse_def;
	}


	

