		private RECLIST_MASK_DEF_CELL DOWN_RECLIST_MASK_DEF_CELL_id_mask_def = null ;






	/**
	 * References the Field ID_MASK_DEF 
	 * Falls keine get_ID_MASK_DEF_cUF() leer ist, wird null zurueckgegeben 
	 */
	public RECLIST_MASK_DEF_CELL get_DOWN_RECORD_LIST_MASK_DEF_CELL_id_mask_def() throws myException
	{
		if (S.isEmpty(this.get_ID_MASK_DEF_cUF()))
			return null;
	
		if (this.DOWN_RECLIST_MASK_DEF_CELL_id_mask_def==null)
		{
			this.DOWN_RECLIST_MASK_DEF_CELL_id_mask_def = new RECLIST_MASK_DEF_CELL (
			       "SELECT * FROM "+bibE2.cTO()+".JD_MASK_DEF_CELL WHERE ID_MASK_DEF="+this.get_ID_MASK_DEF_cUF()+" ORDER BY ID_MASK_DEF_CELL",
			       this.get_oConn());
		}
		return this.DOWN_RECLIST_MASK_DEF_CELL_id_mask_def;
	}





	/**
	 * cZusatzWhere ODER NULL gibt eine weitere auswahl-Bedingung nach unten (z.B.NVL(DELETED,'N')='N')
	 * cORDERKEY ODER NULL bestimmt die sortierung des subselects
	 * buildNew  zwingt zum aufbau einer neuen subquery-list
	 * References the Field ID_MASK_DEF 
	 * Falls keine get_ID_MASK_DEF_cUF() leer ist, wird null zurueckgegeben 
	 */
	public RECLIST_MASK_DEF_CELL get_DOWN_RECORD_LIST_MASK_DEF_CELL_id_mask_def(String cZusatzWhere, String cORDERKEY, boolean buildNew) throws myException
	{
		if (S.isEmpty(this.get_ID_MASK_DEF_cUF()))
			return null;
	
		if (this.DOWN_RECLIST_MASK_DEF_CELL_id_mask_def==null || buildNew)
		{
		    String cQuery = "SELECT * FROM "+bibE2.cTO()+".JD_MASK_DEF_CELL WHERE ID_MASK_DEF="+this.get_ID_MASK_DEF_cUF();
		    if (S.isFull(cZusatzWhere))
		    {
		    	cQuery += (" AND "+cZusatzWhere);
		    }
		    
		    if (S.isFull(cORDERKEY))
		    {
		    	cQuery += (" ORDER BY  "+cORDERKEY);
		    }
		    
			this.DOWN_RECLIST_MASK_DEF_CELL_id_mask_def = new RECLIST_MASK_DEF_CELL (cQuery,this.get_oConn());
		}
		return this.DOWN_RECLIST_MASK_DEF_CELL_id_mask_def;
	}


	

