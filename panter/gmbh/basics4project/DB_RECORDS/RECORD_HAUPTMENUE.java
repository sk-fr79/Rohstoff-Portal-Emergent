		private RECLIST_SERVLETS DOWN_RECLIST_SERVLETS_id_hauptmenue = null ;






	/**
	 * References the Field ID_HAUPTMENUE 
	 * Falls keine get_ID_HAUPTMENUE_cUF() leer ist, wird null zurueckgegeben 
	 */
	public RECLIST_SERVLETS get_DOWN_RECORD_LIST_SERVLETS_id_hauptmenue() throws myException
	{
		if (S.isEmpty(this.get_ID_HAUPTMENUE_cUF()))
			return null;
	
		if (this.DOWN_RECLIST_SERVLETS_id_hauptmenue==null)
		{
			this.DOWN_RECLIST_SERVLETS_id_hauptmenue = new RECLIST_SERVLETS (
			       "SELECT * FROM "+bibE2.cTO()+".JD_SERVLETS WHERE ID_HAUPTMENUE="+this.get_ID_HAUPTMENUE_cUF()+" ORDER BY ID_SERVLETS",
			       this.get_oConn());
		}
		return this.DOWN_RECLIST_SERVLETS_id_hauptmenue;
	}





	/**
	 * cZusatzWhere ODER NULL gibt eine weitere auswahl-Bedingung nach unten (z.B.NVL(DELETED,'N')='N')
	 * cORDERKEY ODER NULL bestimmt die sortierung des subselects
	 * buildNew  zwingt zum aufbau einer neuen subquery-list
	 * References the Field ID_HAUPTMENUE 
	 * Falls keine get_ID_HAUPTMENUE_cUF() leer ist, wird null zurueckgegeben 
	 */
	public RECLIST_SERVLETS get_DOWN_RECORD_LIST_SERVLETS_id_hauptmenue(String cZusatzWhere, String cORDERKEY, boolean buildNew) throws myException
	{
		if (S.isEmpty(this.get_ID_HAUPTMENUE_cUF()))
			return null;
	
		if (this.DOWN_RECLIST_SERVLETS_id_hauptmenue==null || buildNew)
		{
		    String cQuery = "SELECT * FROM "+bibE2.cTO()+".JD_SERVLETS WHERE ID_HAUPTMENUE="+this.get_ID_HAUPTMENUE_cUF();
		    if (S.isFull(cZusatzWhere))
		    {
		    	cQuery += (" AND "+cZusatzWhere);
		    }
		    
		    if (S.isFull(cORDERKEY))
		    {
		    	cQuery += (" ORDER BY  "+cORDERKEY);
		    }
		    
			this.DOWN_RECLIST_SERVLETS_id_hauptmenue = new RECLIST_SERVLETS (cQuery,this.get_oConn());
		}
		return this.DOWN_RECLIST_SERVLETS_id_hauptmenue;
	}


	

