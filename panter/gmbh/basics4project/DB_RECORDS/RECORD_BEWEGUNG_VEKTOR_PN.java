     /**
     * 
     * martin: 2015-04-24: erzeugt ein gefuelltes RECORDNEW_BEWEGUNG_VEKTOR_PN
     * @param msg_sammler
     * @param changeIdWithSeq
     * @param bRemoveAutomaticField
     * @return RECORDNEW_BEWEGUNG_VEKTOR_PN-object filled with values of actual record (to generate copy)
     * @throws myException
     */
     public RECORDNEW_BEWEGUNG_VEKTOR_PN get_Record_NEW_FilledWithActualValues(MyE2_MessageVector msg_sammler, boolean changeIdWithSeq, boolean bRemoveAutomaticField) throws myException {
        if (S.isEmpty(this.get_TABLENAME())) {
            throw new myException(this, "get_RECORDNEW_BEWEGUNG_VEKTOR_PN_FilledWithActualValues(): no Tablename is present !" );
        }
		
        RECORDNEW_BEWEGUNG_VEKTOR_PN  recNew = new RECORDNEW_BEWEGUNG_VEKTOR_PN();
		
        Vector<String> vSonder = this.get_vSonderFelder();
		
        for (String Field: this.keySet()) {
           if (bRemoveAutomaticField) {
                //2015-05-06: geaendert von  bibALL.get_vSonderFelder() nach this.get_vSonderFelder()          
                if (vSonder.contains(Field)) {
                    continue;
                }
            }
            msg_sammler.add_MESSAGE(recNew.set_NewValueForDatabase(Field, this.get_FormatedValue(Field,null)));
        }
		
        if (changeIdWithSeq) {
           recNew.set_NewValueForDatabase_RAW_AS_STRING_IN_STATEMENT(this.get_PRIMARY_KEY_NAME(), "SEQ_"+this.get_TABLE_NAME().substring(3)+".NEXTVAL");
        }
        return recNew;
     }
	
	
