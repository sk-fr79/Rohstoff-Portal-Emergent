	//2013-09-26: statische hash mit insets in Serverumgebung ablegen
	public static Insets I(int left,int top, int right, int bottom) {
		HashMap<String, Insets> hmInsets = bibServer.get_hm_INSETS();
		String cHASH = (""+left)+"_"+ (""+top)+"_"+ (""+right)+"_"+ (""+bottom);
		if (!hmInsets.containsKey(cHASH)) {
			hmInsets.put(cHASH, new Insets(left,top,right,bottom));
		}
		return hmInsets.get(cHASH);
	}
	
	
