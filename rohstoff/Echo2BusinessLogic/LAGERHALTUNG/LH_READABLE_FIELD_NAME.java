 
package rohstoff.Echo2BusinessLogic.LAGERHALTUNG;
  
import nextapp.echo2.app.Alignment;
import panter.gmbh.basics4project.DB_ENUMS.LAGER_BOX;
import panter.gmbh.basics4project.DB_ENUMS.LAGER_PALETTE;
import panter.gmbh.basics4project.DB_ENUMS.VPOS_TPA_FUHRE;
import panter.gmbh.indep.dataTools.IF_Field;
  
  
/*
 * enum: hier koennen die lesbaren feldueberschriften erfasst werden, die dann in liste und maske eingeblendet werden 
 */
public enum LH_READABLE_FIELD_NAME {
	
	ID_LAGER_BOX(LAGER_BOX.id_lager_box,"Box-ID",100,100,new Alignment(Alignment.RIGHT, Alignment.TOP)),
    BOXNUMMER(LAGER_BOX.boxnummer, "Box-NRr.", 100,100, new Alignment(Alignment.CENTER, Alignment.TOP)),
    BESCHREIBUNG(LAGER_BOX.beschreibung, "Beschreibung", 100,100, new Alignment(Alignment.CENTER, Alignment.TOP)),
    BUCHNUNGSNR(LAGER_PALETTE.buchungsnr_hand, "Buchungsnr. Hand", 100, 100, new Alignment(Alignment.CENTER, Alignment.TOP)),
    FUHRE_ID(VPOS_TPA_FUHRE.id_vpos_tpa_fuhre, "Fuhre Id.", 100, 100, new Alignment(Alignment.CENTER, Alignment.TOP))
	;
	
	private IF_Field m_field = null;
	private String   m_readAble = null;
	private int      m_widthLabel = 10;                  //breite fuer die labels
	private int      m_widthComponent = 10;              //breite fuer die komponente
	private Alignment m_align = new Alignment(Alignment.CENTER, Alignment.CENTER);   
	
	private LH_READABLE_FIELD_NAME(IF_Field f, String readAble) {
		this.m_field=f;
		this.m_readAble = readAble;
	}
	
	private LH_READABLE_FIELD_NAME(IF_Field f, String readAble, int widthLabel, int widthComponent, Alignment align) {
		this.m_field=f;
		this.m_readAble = readAble;
		this.m_widthLabel = widthLabel;
		this.m_widthComponent = widthComponent;
		this.m_align = align;
	}
	
	
	
	public static String getReadable(IF_Field f) {
		String ret = "";
		
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_field == f) {
				return rf.m_readAble;
			}
		}
		return ret;
	}
	
	
	public static int getLabelWidth(IF_Field f) {
		int ret = 10;
		
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_field == f) {
				return rf.m_widthLabel;
			}
		}
		return ret;
	}
    
	public static int getComponentWidth(IF_Field f) {
		int ret = 10;
		
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_field == f) {
				return rf.m_widthComponent;
			}
		}
		return ret;
	}
    
	
	public static Alignment getAlignment(IF_Field f) {
		Alignment ret = new Alignment(Alignment.LEFT, Alignment.CENTER);
		
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_field == f) {
				return rf.m_align;
			}
		}
		return ret;
	}
    
	
	public static int getMaxLabelWidth() {
		int i = 10;
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_widthLabel >i) {
				i=rf.m_widthLabel;
			}
		}
		return i;
	}
	
	public static int getMaxComponentWidth() {
		int i = 10;
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_widthComponent >i) {
				i=rf.m_widthComponent;
			}
		}
		return i;
	}
	
    //sucht die groessere breite zwischen label und componenten-breite aus	
    public static int getMaxComponentOrLabelSize(IF_Field f) {
	    int ret = 10;
    		
		for (LH_READABLE_FIELD_NAME  rf: LH_READABLE_FIELD_NAME.values() ) {
			if (rf.m_field == f) {
				if (rf.m_widthLabel>rf.m_widthComponent) {
				   return rf.m_widthLabel;
				} else {
				   return rf.m_widthComponent;
				}
			}
		}
		return ret;
	}
	
}
     
 
